int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_I2C1_Init();
  MX_TIM5_Init();
  MX_USB_DEVICE_Init();
  MX_TIM4_Init();

  /* USER CODE BEGIN 2 */
	
	LTC_init(hi2c1);
	oled_init();
	HAL_Delay(50);
	
	
	HAL_TIM_Base_Start_IT(&htim4);
	HAL_TIM_Base_Start_IT(&htim5);
	
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		
				//whole loop takes about 80ms to complete because long i2c writes to oled
		update_usb_led();
		
		LTC_getdata(&temperature,&charge,&voltage,&current); //read data from power meter
		
		char buff[20];
		
		//clear gddr
		clear();
		
		if(startup<32)
		{
			draw_tafr(31,0);
			graphics_text(0,0,FONT_TEN_DOT,"V0.99");
			startup++;
		}
		else
		{
			//voltage,current,power,temperature
			sprintf(buff,"%4.1fV",voltage);
			graphics_text(0,0,FONT_TEN_DOT,buff);
			sprintf(buff,"%4.1fA",current);
			graphics_text(0,12,FONT_TEN_DOT,buff);
			float power = voltage*current;
			sprintf(buff,"%4.1fW",power);
			graphics_text(84,0,FONT_TEN_DOT,buff);
			sprintf(buff,"%4.1fC",temperature);
			graphics_text(84,12,FONT_TEN_DOT,buff);
			line_h(0,127,25,1,add);
			line_v(0,25,48,1,add);
			line_v(0,25,78,1,add);
			
			sprintf(buff,"%5.0fmAh",charge);
			graphics_text(92,58,FONT_FIVE_DOT,buff);
			
			if(counter4++>32)
			{
				counter4 = 0;
				display_state++;
				if(display_state==11)
					display_state = 0;
			}
			
			if(display_state<4)
				sprintf(buff,"OUT%d: %d",display_state+1,outputs[display_state]);
			else if(display_state<8)
				sprintf(buff,"PWM%d: %d",display_state-3,pwm[display_state-4]);
			else
				sprintf(buff,"SERVO%d: %d",display_state-7,rc_pwm[display_state-8]);

			graphics_text(0,58,FONT_FIVE_DOT,buff);
			
			line_h(0,127,56,1,add);
			line_v(56,63,63,1,add);
			
			//usb
			if(is_usb_connected())
				draw_usb(50,0);
			
			
			//interface
			
			if(HAL_GPIO_ReadPin(M_enable_GPIO_Port,M_enable_Pin))
			{			
				if(counter3<startup_time)
				{
					//graphics_text(30,32,FONT_TEN_DOT,"STARTING");
					line_h(5,123,41,12,add);
					line_h(7,121,41,8,erase);
					line_h(9,9+110*counter3/startup_time,41,4,add);
					counter3++;
				}
				else
				{
					graphics_text(30,34,FONT_FIFTEEN_DOT,"ARMED");
					if(counter2++%32<20)
					{
						graphics_text(15,34,FONT_FIFTEEN_DOT,"!");
						graphics_text(110,34,FONT_FIFTEEN_DOT,"!");
					}
				}
				counter = 0;
			}
			else
			{
				if(counter5%32<25)
				{
					if(remote_emg)
						graphics_text(2,30,FONT_TEN_DOT,"RELEASE REMOTE");
					else
						graphics_text(15,30,FONT_TEN_DOT,"RELEASE EMG");
					graphics_text(15,43,FONT_TEN_DOT,"PRESS POWER");
				}

				counter2 = 0;
				counter3 = 0;
			}
		}
		
		oled_update();
		
		HAL_Delay(50);
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Configure the main internal regulator output voltage 
    */
  __HAL_RCC_PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE2);

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLM = 12;
  RCC_OscInitStruct.PLL.PLLN = 168;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV4;
  RCC_OscInitStruct.PLL.PLLQ = 7;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV2;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_2) != HAL_OK)
  {
    Error_Handler();
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

