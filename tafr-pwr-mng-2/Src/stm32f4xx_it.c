/**
* @brief This function handles TIM5 global interrupt.
*/
void TIM5_IRQHandler(void)
{
  /* USER CODE BEGIN TIM5_IRQn 0 */
	/* 1kHz interrupt */
	
	if(input_capture>1500 && input_capture < 2500)
		remote_emg = 0;
	else
		remote_emg = 1;
	
	uint8_t i;
	
	for(i=0;i<8;i++) //set pwm for outputs 1-8
		set_pwm(i,pwm[i]);
	
	for(i=0;i<3;i++) //set rc pwm for outputs 1-3
		set_rc_pwm(i,rc_pwm[i]);
	
	HAL_GPIO_WritePin(DIS_MOTOR_GPIO_Port,DIS_MOTOR_Pin,motors_sw_disable); //disable motors with software
	armed = HAL_GPIO_ReadPin(M_enable_GPIO_Port,M_enable_Pin);
	HAL_GPIO_WritePin(GPIOA,GPIO_PIN_5,!armed);
	
	//if(HAL_GPIO_ReadPin(M_enable_GPIO_Port,M_enable_Pin) && !HAL_GPIO_ReadPin(PG_GPIO_Port,PG_Pin))
	if(HAL_GPIO_ReadPin(M_enable_GPIO_Port,M_enable_Pin))
		timeout_pg++;
	else
	{
		timeout_pg = 0;
		motors_sw_disable = 0;
	}
	
	if(remote_emg==1)
	{		
		motors_sw_disable = 1;
	}
  /* USER CODE END TIM5_IRQn 0 */
  HAL_TIM_IRQHandler(&htim5);
  /* USER CODE BEGIN TIM5_IRQn 1 */

  /* USER CODE END TIM5_IRQn 1 */
}

